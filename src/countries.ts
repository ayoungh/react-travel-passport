export const DEFAULT_COVER: CoverTheme = COUNTRY_COVERS.world;

export function resolveCover(
  country?: string,
  overrides?: Partial<CoverTheme>,
): CoverTheme {
  const preset =
    (country && COUNTRY_COVERS[country.toLowerCase()]) || DEFAULT_COVER;
  return { ...preset, ...overrides };
}
